<strong><h2>Endokrinologi</h2></strong>

	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<img src="endokrinologi.jpg" >

	<br><br>
	<hr size="3px" width="810px" color="grey" align="left" >	
<br>
<br><strong>LH</strong>
<br>LH atau Luteinizing Hormone atau gonadotropin dihasilkan oleh sel-sel gonad yang berada pada kelenjar pituitari di dasar otak dan pengeluaran hormon ini dirangsang oleh gonadotropin releasing hormone (GnRH). 
<br>LH pada wanita berfungsi untuk membantu pelepasan sel telur dari folikel, LH pada pria berfungsi untuk merangsang testis dalam mensintesis hormon steroid dan merangsang produksi testosteron pada sel Leydig.
<br>Pemeriksaan LH adalah untuk mengukur konsentrasi kadar LH dalam darah yang dapat memperkirakan terjadinya ovulasi;mengevaluasi infertilitas; dan mendiagnosis gangguan gonad atau pituitari.
<br>Persiapan    : Sebaiknya pemeriksaan Hormon dilakukan pada pagi hari sebelum jam 10 pagi, tidak perlu puasa.
<br>
<br><strong>FSH</strong>
<br>FSH atau folicle stimulating hormone atau gonadotropin dihasilkan oleh sel-sel gonad yang berada pada kelenjar pituitari di dasar otak dan pengeluaran hormon ini dirangsang oleh gonadotropin releasing hormone (GnRH).
<br>FSH pada wanita berfungsi untuk merangsang pematangan folikel,
<br>FSH pada pria berfungsi untuk merangsang aktivitas di tubulus seminiferus seperti spermatogenesis, merangsang sekresi estrogen pada sel sertoli dan memperkuat efek luteinizing hormone (LH) dalam merangsang sel Leydig.
<br>Pemeriksaan kadar FSH adalah untuk mengevaluasi infertilitas dan mendiagnosis gangguan gonad atau pituitari.
<br>Persiapan    : Sebaiknya pemeriksaan Hormon dilakukan pada pagi hari sebelum jam 10 pagi, tidak perlu puasa.
<br>
<br><strong>Prolaktin</strong>
<br>Pemeriksaan untuk mengukur kadar prolaktin, yang memperngaruhi infertilitas dan untuk mengetahui ada tidaknya gangguan gonad ptituari.
<br>Persiapan  : Sebaiknya pemeriksaan Hormon dilakukan pada pagi hari sebelum jam 10 pagi dan telah berpuasa 10-12  jam sebelumnya
<br>
<br><strong>Testosteron</strong>
<br>Testosterone adalah hormone penting dalam proses reproduksi dan dihasilkan di testis yang berfungsi mempengaruhi perkembangan sifat seks sekunder pria.
<br>Fungsi testosteron yaitu mempengaruhi perkembangan sifat-sifat seks sekunder pria, memberikan feedback negatif melalui pituitari dan hipotalamus sehingga menghasilkan penurunan sekresi luteinizing hormone (LH) dan menjaga fungsi kelenjar prostat.
<br>Pemeriksaan testosterone adalah untuk mengetahui kadar testosterone dalam darah yang berfungsi mengevaluasi fungsi gonad dan adrenal, mengetahui penyebab pubertas dini pada pria, dan mendiagnosis hipogonadisme, hipopituarisme, sindrom Klinifelter, dan impoten. 
<br>Persiapan    : Sebaiknya pemeriksaan Hormon dilakukan pada pagi hari sebelum jam 10 pagi, tidak perlu puasa.
