<br><strong>Estradiol</strong>
<br>Pemeriksaan estradiol adalah untuk mengukur kadar hormon estradiol yang berfungsi untuk mempertebal dinding rahim guna mempersiapkan rahim sebagai tempat bertumbuhnya janin. 
<br>Hormon ini estradiol diproduksi di ovarium dan pengeluarannya dipengaruhi oleh siklus menstruasi. Menjelang ovulasi, kadarnya akan menjadi sangat tinggi, tetapi setelah ovulasi akan menurun dengan cepat.
<br>Persiapan    : Sebaiknya pemeriksaan Hormon dilakukan pada pagi hari sebelum jam 10 pagi, tidak perlu puasa.
<BR>
<br><strong>Progesteron</strong>
<br>Progesteron adalah hormone yang berperan dalam menyediakan kebutuhan darah dan nutrisi pada dinding rahim.
<br>Pemeriksaan progesterone adalah untuk memastikan terjadinya ovulasi dan perkiraan fase luteal,, resiko abortus spontan pada kehamilan dini. 
<br>Persiapan    : Sebaiknya pemeriksaan Hormon dilakukan pada pagi hari sebelum jam 10 pagi, tidak perlu puasa.
<br>
<br><strong>Growth Hormon (GH)</strong>
<br>Pemeriksaan Growth hormone (GH) atau hormon pertumbuhan adalah untuk  mengukur konsentrasi GH. 
<br>Produksi GH akan berkurang seiring dengan bertambahnya usia, dan kekurangan GH dapat mempengaruhi kondisi fisik dan mental seseorang.
<br>Persiapan    : Sebaiknya pemeriksaan Hormon dilakukan pada pagi hari sebelum jam 10 pagi, tidak perlu puasa.
