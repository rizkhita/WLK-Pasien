<strong><h2>Imunologi dan Serologi</h2></strong>

	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<img src="imunologi.jpg" >

	<br><br>
	<hr size="3px" width="810px" color="grey" align="left" >	
<br>
<br><strong>Anti Toxoplasma IgG</strong>
<br>Pemeriksaan Anti-Toxoplasma IgG adalah untuk mengetahui adanya antibodi IgG terhadap parasit Toxoplasma gondii yang dapat menginfeksi janin. Anti-Toxoplasma IgG muncul 1-2 minggu setelah infeksi primer dan mencapai konsentrasi puncak dalam waktu 4-8 minggu. Konsentrasi IgG dalam darah dapat menurun setelah beberapa bulan atau tahun dan akan menetap seumur hidup dengan konsentrasi rendah. 
<br>IgG dapat melewati plasenta dan pada neonatus, IgG yang berasal dari ibu dapat bertahan kurang lebih selama 6 bulan. Bayi akan mulai membentuk antibodi IgG sendiri pada usia 2-3 bulan.
<br>Persiapan     : Tidak memerlukan persiapan khusus
<br>
<br><strong>Anti Toxoplasma IgM</strong>
<br>Pemeriksaan Anti-Toxoplasma IgM adalah untuk  mengetahui adanya antibodi IgM terhadap parasit Toxoplasma gondii.
<br>Anti-Toxoplasma IgM muncul 5 hari setelah infeksi, dan peningkatan konsentrasi terus menerus hingga  minggu ke 4.
<br>Untuk kasus IgM-Non spesifik, IgM akan menetap dalam dalam tubuh hingga lebih dari 6 bulan. 
<br>Persiapan     : Tidak memerlukan persiapan khusus
<br>
<br><strong>Avidity Anti Toxoplasma IgG</strong>
<br>Pemeriksaan Aviditas anti-Toxoplasma IgG adalah untuk memperkirakan kapan terjadinya infeksi toxoplasma terjadi. Diperiksa apabila hasil Anti toxoplasma IgG dan IgM keduanya positif.
<br>Persiapan     : Tidak memerlukan persiapan khusus
<br>
<br><strong>Anti Rubella IgG</strong>
<br>Pemeriksaan Anti-Rubella IgG adalah  untuk mengetahui keberadaan antibodi IgG terhadap virus Rubella.
<br>Pemeriksaan ini dilakukan untuk sebagai uji saring infeksi Rubella; memantau respon tubuh terhadap vaksinasi rubella, dan mendiagnosis infeksi Rubella aktif (peningkatan konsentrasi IgG yang signifikan)
<br>Persiapan     : Tidak memerlukan persiapan khusus
<br>
<br><strong>Anti Rubella IgM</strong>
<br>Pemeriksaan Anti-Rubella IgM adalah untuk mengetahui adanya antibodi IgM terhadap virus Rubella. Infeksi Rubella primer yaitu infeksi pada wanita hamil dan janinnya harus dikonfirmasi dengan peningkatan konsentrasi IgG.
<br>Persiapan     : Tidak memerlukan persiapan khusus
<br>
<br><strong>Anti CMV IgG</strong>
<br>Pemeriksaan Anti-CMV IgG adalah untuk  mendeteksi keberadaan antibodi  IgG terhadap Cytomegalovirus.
<br>Pemeriksaan ini dilakukan untuk mendiagnosa infeksi CMV lampau (jika kadar IgG Stabil), mendiagnosa infeksi CMV aktif / reinfeksi ( jika kadar  IgG meningkat); serta mengidentifikasi carrier infeksi CMV sebelum menjadi donor darah atau organ.
<br>Persiapan     : Tidak memerlukan persiapan khusus
<br>
<br><strong>Anti CMV IgM</strong>
<br>Pemeriksaan Anti-CMV IgM adalah untuk medeteksi keberadaan antibodi IgM terhadap Cytomegalovirus.
<br>Pemeriksaan ini dilakukan untuk mendiagnosa  infeksi CMV primer atau reinfeksi CMV (pada wanita hamil dan janinnya dan juga pada pasien transplantasi).
<br>Persiapan     : Tidak memerlukan persiapan khusus
