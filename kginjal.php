<br><strong>Faal Ginjal & Asam Urat</strong>
<br>
<br><strong>Creatinin</strong>
<br>Pemeriksaan Creatinin untuk mengetahui kadar kreatinin dalam darah yang menunjukkan kondisi ada tidaknya kerusakan pada ginjal. 
<br>Persiapan  : Tidak memerlukan persiapan khusus
<br>
<br><strong>BUN / Urea</strong>
<br>Pemeriksaan Ureum adalah untuk mengetahui perbandingan produksi dan klirens dari Urea sebagai hasil metabolisme akhir protein di hati dan dikeluarkan di ginjal. Peningkatan kadar Urea dalam darah terjadi pada penyakit ginjal akut dan kronis
<br>Pemeriksaan Urea dapat digunakan pula untuk pemantauan hemodyalisis dan terapi lainnya
<br>Persiapan  : Tidak memerlukan persiapan khusus
<br>
<br><strong>Asam Urat</strong>
<br>Pemeriksaan untuk mengetahui kadar asam urat dalam darah. Pemeriksaan ini akan membantu diagnosa gout, pengobatan pada pasien dengan asam urat tinggi, serta pasien kemoterapi.
<br>Persiapan : Sebaiknya puasa selama 8-12 jam
<br>
<br><strong>Cystatin C</strong>
<br>Pemeriksaan cystatin C adalah untuk mengevaluasi fungsi ginjal dalam menyaring darah. 
<br>Proses penyaringan atau laju filtrasi glomerulus (LFG) dapat dicerminkan dari kadar cystatin C dalam darah.
<br>Persiapan : Tidak memerlukan persiapan khusus.
