<p>	
<br><strong>Glukosa Darah</strong>
<br>
<br><strong>Glukosa Sewaktu</strong>
<br>Pemeriksaan Glukosa sewaktu adalah untuk mengetahui kadar glukosa dalam darah pada saat  sampling dilakukan.
<br>Pemeriksaan Glukosa sewaktu dapat menskrining dan mendiagnosis DM, pemantauan terapi DM, dan mendeteksi serta menangani jika terdapat gangguan metabolik lainnya.
<br>Persiapan    : Tidak memerlukan persiapan khusus
<br>
<br><strong>Glukosa Puasa</strong>
<br>Pemeriksaan Glukosa setelah melakukan puasa 8-12 jam sebelumnya akan megukur kadar glukosa dalam darah pada kondisi basal tubuh /  endogen    
<br>Pemeriksaan Glukosa puasa dapat menskrining dan mendiagnosis DM, pemantauan terapi DM, dan mendeteksi serta menangani jika terdapat gangguan metabolik lainnya.
<br>Persiapan    : Pasien puasa selama 8 -12 jam. 
<br>
<br><strong>Glukosa 2 JPP</strong>
<br>Pemeriksaan glukosa 2 JPP adalah pemeriksaan  kadar glukosa setelah pemberian sejumlah glukosa yang setara dengan 75 g glukosa selama 2 (dua) jam.
<br>Pemeriksan Glukosa 2JPP dilakukan untuk mengetahui aktivitas insulin dalam tubuh yang juga dapat menskrining dan mendiagnosis DM, pemantauan terapi DM, dan mendeteksi serta menangani jika terdapat gangguan metabolik lainnya.
<br>Persiapan    : Sampel diambil setelah pasien melakukan puasa selama 2 (dua) jam terakhir setelah mengkonsumsi makanan tertentu. Sebaiknya untuk tidak melewakti masa toleransi 10 menit setelah 2 jam berpuasa untuk memberikan hasil pemeriksaan yang lebih akurat.
<br>
<br><strong>Glukosa Toleransi Test (GTT) 2 Jam</strong>
<br>Pemeriksaan GTT 2 jam adalah untuk mengetahui kadar glukosa darah 2 jam setelah melakukan puasa untuk menilai pasien yang dicurigai Pradiabetes / Toleransi terhadap glukosa / resiko Diabetes Mellitus.
<br>Persiapan    : Sampel diambil setelah pasien melakukan puasa selama 2 (dua) jam terakhir setelah mengkonsumsi glukosa yang telah ditakar dalam jumlah tertentu  Sebaiknya untuk tidak melewakti masa toleransi 10 menit setelah 2 jam berpuasa untuk memberikan hasil pemeriksaan yang lebih akurat.
<br>
<br><strong>HbA1c</strong>
<br>Pemeriksaan HbA1c adalah pemeriksan konsentrasi kadar glukosa darah rata-rata selama 1-3 (tiga) bulan sebelum pemeriksaan. Pemeriksaan kadar HbA1c dapat membantu memberikan penanganan DM yang tepat dan optimal sehingga mengurangi resiko terjadinya komplikasi DM yang lebih parah.
<br>Persiapan    : Tidak memerlukan persiapan khusus
<br>
<br><strong>Insulin</strong>
<br>Pemeriksaan insulin adalah untuk mengetahui kadar hormone insulin dalam darah. Untuk mengetahui penyebab terjadinya hipoglikemia atau hiperglikemia. 
<br>Persiapan    : Tidak memerlukan persiapan khusus
