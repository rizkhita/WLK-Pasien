<p>	
<br><strong>Faal Liver</strong>
<br>
<br><strong>SGOT</strong>
<br>Pemeriksaan SGOT adalah pemeriksaan kadar Enzim Glutamic Oxaloacetic Transaminase dalam tubuh yang berhubungan dengan sel parenkim hati. Pemeriksaan SGOT tidak spesifik untuk mendeteksi kerusakan pada hati karena juga dihasilkan oleh sel Jantung dan sel otot skelet.
<br>Persiapan    : Tidak memerlukan persiapan khusus.

<br><br><strong>SGPT</strong>
<br>Pemeriksaan SGPT adalah pemeriksaan Enzim Glutamic Pyruvic Transaminase atau Alanine Aminotransferase (ALT) dalam tubuh. Enzim GPT / ALT sebagian besar terdapat dalam hati, selain itu sebagian kecil lainnya juga terdapat dalam organ jantung ginjal dan sel otot.
<br>ALT / GPT lebih spesifik dalam mendeteksi kerusakan hati daripada GPT. Jumlah sel hati yang mati tidak berkaitan dengan peningkatan konsentrasi kadar ALT/GPT sehingga  pada kerusakan hati yang cukup parah tidak menunjukkan peningkatan ALT dalam darah
<br>Persiapan    : Tidak memerlukan persiapan khusus.
 
<br><br><strong>Gamma GT</strong>
<br>Pemeriksaan Gamma GT (GGT) untuk mengetahui kadar GGT dalam darah. GGT adalah enzim yang palin gbanyak ditemukan di hati, saluran empedu dan ginjal. Gamma GT yang lolos sampai ke aliran darah menjadi penanda sensitif untuk kerusakan saluran empedu dan membantu mengevaluasi fungsi hati.
<br>Persiapan    : Tidak memerlukan persiapan khusus

<br><br><strong>Alkali Fosfatase (ALP)</strong>
<br>Pemeriksaan ALP (Alkali Phosphatase) untuk mengetahui kadar ALP dalam darah. ALP disintesis oleh hati dan disekresikan masuk ke salurna empedu. 80% ALP dalam darah berasal dari hati, dan tulang, selebihnya disintesa oleh ginjal, plasenta dan usus.
<br>Persiapan    : Tidak memerlukan persiapan khusus.

<br><br><strong>Protein Total</strong>
<br>Pemeriksaan Protein total adalah untuk mengetahui kadar protein total dalam darah. Protein dalam darah terdiri dari Albumin dan globulin. Hasil pemeriksaan Protein total dapat digunakan untuk mendeteksi adanya penyakit dan gangguan hati serta status nutrisi.
<br>Persiapan    : Tidak memerlukan persiapan khusus

<br><br><strong>Albumin</strong>
<br>Pemeriksaan untuk mengetahui kadar albumin dalam darah untuk mendeteksi dan mengevaluasi kondisi dan gangguan fungsi hati, ginjal serta malnutrisi.
<br>Persiapan    : Tidak memerlukan persiapan khusus

<br><br><strong>Bilirubin Total</strong>
<br>Pemeriksaan bilirubin total untuk mengetahui total kadar bilirubin dalam darah baik yang terkonjugasi maupun yang tidak terkonjugasi. Biliburin tidak terkonjugasi tidak larut dalam air, sehingga tidak ditemukan dalam pemeriksaan urine.
<br>Persiapan    : Tidak memerlukan persiapan khusus

<br><br><strong>Bilirubin Direk</strong>
<br>Pemeriksaan Bilirubin direk untuk mengetahui kadar bilirubin terkonjugasi dalam darah.
<br>Bilirubin adalah pecahan haem (darah) yang akan berikatan dengan albumin dan bersikulasi dalam darah, kemudian dikonjugasi dan disekresi oleh hati. Karena bilirubin direk adalah bilirubin yang terkonjugasi sehingga bersifat larut dalam air, maka dapat pula ditemukan dalam urine
<br>Persiapan    : Tidak memerlukan persiapan khusus

<br><br><strong>LDH</strong>
<br>Pemeriksaan LDH adalah untuk membedakan diagnose serangan jantung , anemia , kerusakan paru , penyakit hati dari kondisi lain yang dapat menyebabkan gejala serupa ( Diagnosis differensial ) .
<br>Persiapan    : Tidak memerlukan persiapan khusus

<br><br><strong>Amylase Pancreatic</strong>
<br>Pemeriksaan Amylase pancreatic adalah untuk mendiagnosis kondisi pankreatitis akut
<br>Gejala Pancreatitis adalah abdominal pain, epigastric tenderness,nausea, dan vomiting. 
<br>Amilase digunakan untuk membedakan diagnosis pankreatitis akut dan kronik, ada atau tidaknya pada individu alkoholisme. 
<br>Persiapan    : Tidak memerlukan persiapan khusus

<br><br><strong>Lipase</strong>
<br>Pemeriksaan Lipase adalah untuk memperkirakan kondisi pancreatitis akut dan kronis
<br>Persiapan    : Tidak memerlukan persiapan khusus

