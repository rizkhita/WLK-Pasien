<p>	
<br><strong>Lemak Darah</strong>
<br>
<br>Kolesterol dalam rentang konsentrasi normal memiliki fungsi :
<br>a.  Komponen essensial dari sel
<br>b.  Sebagai Prekursor / bahan dasar pembentuk asam empedu, steroid adrenal, dan hormon-hormon estrogen dan androgen
<br>
<br><strong>Kolesterol Total</strong>
<br>Pemeriksaan Koletserol Total untuk mengetahui kadar total kolesterol yang terdapat dalam darah. Total kolesterol berasal dari semua partikel lipoprotein tubuh baik kolesterol maupun trigliserida
<br>Mengetahui kadar kolesterol total dapat mendeteksi gangguan metabolisme lemak dan menjadi faktir resiko PJK 
<br>Persiapan    : Tidak memerlukan persiapan khusus
<br>
<br><strong>Kolesterol LDL Direk</strong>
<br>Pemeriksaan Kolesterol LDL untuk mengetahui kadar kolesterol LDL dalam darah. Kolesterol LDL (Low Density Lipoprotein) terbentu dari penyerapan makanan d usus, memiliki densitas rendah, mudah menggumpal dan menempel pada dinding pembuluh darah.
<br>Sering disebut kolesterol jahat karena dapat menempel di dinding pembuluh darah sehingga membentuk plak aterosklerosis yang akan mempersempit pembuluh darah.
<br>Persiapan    : Tidak memerlukan persiapan khusus
<br>
<br><strong>Kolesterol HDL</strong>
<br>Pemeriksaan Kolesterol HDL untuk mengetahui kadar kolesterol HDL dalam darah. Kolesterol HDL (High Density Lipoprotein) dibentuk di hati,memiliki densitas yang tinggi sehingga tidak mudah menggumpal .
<br>Sering disebut sebagai kolesterol baik karena fungsinya membantu membersihkan tumpukan kolesterol-koletserol dari pembuluh darah untuk diangkut kembali ke hati.
<br>Persiapan    : Tidak memerlukan persiapan khusus
<br>
<br><strong>Trigliserida</strong>
<br>Pemeriksaan Trigliserida adalah untuk mengetahui kadar konsentrasi trigliserida dalam darah. Trigliserida adalah bentuk dari lemak yang tersimpan dalam tubuh dan banyak ditemukan di jaringan adipose.
<br>Persiapan    : Karena pemeriksaan trigliserida sangat dipengaruhi kadarnya oleh asupan makanan dari luar, maka disarankan untuk puasa 10-12 jam sebelum melakukan pemeriksaan trigliserida.
.
<br>
<br><strong>Apo B</strong>
<br>Pemeriksaan Apo B adalah untuk mengetahui konsentrasi kadar Apo-B 100 dalam darah yang penting dalam proses aterosklerosis dan peningkatannya potensial meningkatkan resiko PJK.
<br>Setiap partikel LDL mengandung 1(satu) molekul Apo-B, sehingga mengukur kadar Apo-B menggambarkan jumlah partikel LDL dalam tubuh.
<br>Pemeriksaan Apo-B akan melengkapi pemeriksaan Lemak darah yang dimana sangat dianjurkan untuk menilai kondisi dislipidemia dan jumlah partikel Small Dense LDL, serta mampi menentukan keberhasilan terapi statin.
<br>Persiapan  : Sebaiknya puasa selama 8-12 jam
<br>
<br><strong>Small Dense LDL</strong>
<br>Pemeriksaan Small Dense LDL adalah untuk mengetahui resiko seseorang mengalami PJK /Stroke.
<br>Nilai < 1,2 menunjukkan seseorang memiliki resiko tinggi PJK/Stroke.
<br>Small Dense LDL adalah LDL dengan ukuran yang sangat kecil < 25,5 nm , padar, bersifat aterogenik,dan memiliki affinitas terhadap LDL-R yang rendah sehingga partikel Small Dense LDL lebih lama bersirkulasi dalam darah. Sifat Small Dense LDL tersebut memudahkannya untuk masuk dalam lapisan pembuluh darah dan mengendap serta teroksidasi oleh radikal bebas membentuk LDL teroksidasi. 
<br>Keberadaan small dense LDL ini berkaitan dengan peningkatan konsentrasi trigliserida dan Apo B, serta penurunan konsentrasi HDL
<br>Persiapan    : Sebaiknya pasien puasa 10-12 Jam sebelum pemeriksaan.
<br>
<br><strong>Apo A1</strong>
<br>Pemeriksaan Apo A-1 adalah untuk menilai kualitas dari kolesterol HDL. 
<br>Persiapan    : Tidak memerlukan persiapan khusus
<br>
<br><strong>Lp (a)</strong>
<br>Pemeriksaan Lp(a) adalah untuk melihat resiko tunggal penyebab resiko PJK Stroke. Lp(a) bersifat genetic sehingga pemeriksaan Lp(a) tidak membutuhkan persiapan sebelumnya.
