<p>
	
<br><strong>Hematologi Lengkap</strong>
<br>Hematologi lengkap adalah penilaian dasar komponen dan unsur-unsur sel darah sebagai pemeriksaan screening awal maupun pemeriksaan lanjutan untuk mengetahui dan menindaklanjuti kelainan hematologi
<br>Persiapan    : Tidak ada persiapan khusus sebelum pemeriksaan
<br>Hematologi Lengkap akan menunjukkan kadar :
<br>
<br><strong>Hematokrit (HCT)</strong>
<br>Pemeriksaan    : Perbandingan persentase sel darah merah, sel darah putih dan sel trombosit dengan plasma darah
<br>
<br><strong>Hemoglobin (HB)</strong>
<br>Pemeriksaan    : Menentukan konsentrasi hemoglobin dalam volume darah yang bertugas membawa oksigen dalam sel darah merah.
<br>
<br><strong>Eritrosit</strong>
<br>Pemeriksaan    : Untuk mengetahui kondisi dan kelainan sel darah merah yang merupakan alat transport utama O2
<br>Kadar yang terlalu rendah karena kecepatan hemolisis darah yang lebih cepat daripada proses pembentukan sel darah merah di sumsum tulang belakang dapat menyebabkan anemia
<br>
<br><strong>MCV</strong>
<br>Pemeriksaan    : untuk menentukan volume rata rata sebuah erotrosit
<br>
<br><strong>MCH</strong>
<br>Pemeriksaan    : Untuk mengetahui rata rata banyaknya Hemoglobin yang terdapat dalam eritrosit
<br>
<br><strong>MCHC</strong>
<br>Pemeriksaan    : Untuk menentukan konsentrasi hemoglobin pada volume eritrosit.
<br>
<br><strong>Leukosit</strong>
<br>Pemeriksaan    : Untuk mengetahui kelainan sel darah putih yang bertanggung jawab dalam imunitas tubuh
<br>
<br>Hitung Jenis Leukosit:
<br>
<br><strong>Neutrofil</strong>
<br>Neutrofil berperan dalam melawan infeksi, meningkat pada kondisi inflamasi paru-paru, leukimia, luka bakar, tekanan darah tinggi pada kehamilan dan hemolisis.
<br>
<br><strong>Limfosit</strong>
<br>Limfosit berperan dalam menghasilkan antibodi untuk mengatasi infeksi, meningkat pada kondisi hepatitis karena infeksi, TBC dan penyakit karena virus lainnya.
<br>
<br><strong>Monosit</strong>
<br>Monosit berperan dalam sistem imun, meningkat pada kondisi leukimia, TBC dan infeksi peradangan kronis
<br>
<br><strong>Eosinofil</strong>
<br>Eosinofil meningkat pada reaksi alergi dan infeksi parasit, pemnyakit kolagen, anemia, dsb
<br>
<br><strong>Basofil</strong>
<br>Basofi berperan dalam proses imunologi dan inflamasi 
<br>
<br><strong>RDW</strong>
<br>Pemeriksaan    : Untuk mengetahui kondisi perbedaan ukuran dari eritrosit/sel darah merah
<br>
<br><strong>Trombosit   </strong> 
<br>Pemeriksaan    : Untuk mengetahui adanya kelainan dalam proses pembekuan darah, mengevaluasi dan memantau pendarahan, leukimia, dll.
<br>
<br><strong>LED    </strong>
<br>Pemeriksaan    : Sebagai indikator infeksi dan atau inflamasi yang tidak spesifik, mampu membedakan tingkat keradangan.
<br>
