	<p>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Laboratorium Klinik Al-Muhtar didirikan pertama kali di Bandung, Jawa Barat, pada tanggal 7 Mei 1973 oleh beberapa orang idealis berlatar belakang pendidikan farmasi. Sejak awal, Dr. Deden Muhtar, MBA beserta seluruh pendiri lainnya tetap menjaga komitmen untuk mempersembahkan hasil pemeriksaan terbaik dengan layanan sepenuh hati. 
