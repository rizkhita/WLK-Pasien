<br><strong><h3>A.&nbsp; Urine Lengkap</h3></strong>
Pemeriksaan Urine Lengkap dapat mendiagnosis dan memantau gangguan pada saluran kemih dan ginjal.
<br>Persiapan    : Urine yang ditampung sebaiknya adalah urine pertama pada pagi hari, urine harus dalam kondisi segar pada saat dilakukan pemeriksaan.
<br>Oleh karena itu, waktu penyerahan urine sampai ke tangan laboratories tidak boleh lebih dari 1 (satu) jam.
<br>
<br>Pemeriksaan urin lengkap meliputi : 
<br>
<br><strong>1.&nbsp;   Kimia</strong> 
<br>
<br>&nbsp;Berat Jenis
<br>&nbsp;pH
<br>&nbsp;Leukosit esterase, 
<br>&nbsp;Nitrit Urine
<br>&nbsp;Glukosa Urine
<br>&nbsp;Keton Urine
<br>&nbsp;Urobilinogen Urine
<br>&nbsp;Biliubin Urine
<br>&nbsp;Lainnya
<br>
<br><strong>2.&nbsp;   Sedimen Mikroskopis</strong>
<br>
<br>&nbsp;Leukosit
<br>&nbsp;Eritrosit
<br>&nbsp;Silinder
<br>&nbsp;Epitel Sel
<br>&nbsp;Bakteri
<br>&nbsp;Telur Cacing
<br>&nbsp;Kristal
<br>
<br><strong>3.&nbsp;   Makroskopis</strong>
<br>&nbsp;
<br>&nbsp;Warna Urine
<br>&nbsp;Kejernihan Urine
<br>
<br>
<br><strong><h3>B.&nbsp;  Test Kehamilan</h3></strong>
Pemeriksaan ini dilakukan untuk mendeteksi ada tidaknya hormon human chorionic gonadotropin (hCG) di dalam darah yang dapat menunjukkan ada tidaknya kehamilan.
<br>Hormon hCG diproduksi oleh plasenta selama masa kehamilan, dan dapat terdeteksi di dalam darah pada masa kehamilan paling awal, bahkan sebelum periode menstruasi berhenti.

